  Widget rightTitleWidgets(double value, TitleMeta meta) {
    String text;
    switch (value.toInt()) {
      case 0:
        text = '0';
        break;
      case 2:
        text = '150';
        break;
      case 4:
        text = '300';
        break;
      case 6:
        text = '450';
        break;
      case 8:
        text = '600';
        break;
      case 10:
        text = '750';
        break;
      default:
        return Container();
    }

    return Text(text,
        style: TextStyle(
          color: TColor.gray,
          fontSize: 12,
        ),
        textAlign: TextAlign.center);
  }

